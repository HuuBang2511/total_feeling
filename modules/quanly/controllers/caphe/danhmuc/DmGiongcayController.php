class DmGiongcayController extends QuanlyBaseController
{

    public $title = "Danh mục giống cây";

    public $const;

    public function init(){
        parent::init();
            $this->const = [
            'title' => 'Danh mục giống cây',
            'label' => [
                'index' => 'Danh sách',
                'create' => 'Thêm mới',
                'update' => 'Cập nhật',
